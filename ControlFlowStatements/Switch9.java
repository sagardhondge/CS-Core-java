class Switch9 
{
	public static void main(String[] args) 
	{	char signal=97;
		switch (signal)
		{
		case 'a':System.out.println("A");
				break;
		case 'b':System.out.println("B");
				break;
		case 'c':System.out.println("C");
				break;
		default :System.out.println("Default");
				break;
		}
		
	}
}

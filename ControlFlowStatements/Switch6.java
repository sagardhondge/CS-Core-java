class Switch6
{
	public static void main(String[] args)
	{
		System.out.println("Start @ Cyber Success");

		int a = 5;

		switch (a)
		{
			case 5:
				System.out.println("Inside Switch");
				break;

			default:
				System.out.println("No Match Found");
		}

		System.out.println("Stop @ Cyber Success");
	}
}

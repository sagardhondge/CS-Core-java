//Arithemetic operator
class operator1 
{
	public static void main(String[] args) 
	{	int result;
		// addiotion
		result=10+20;
		System.out.println("The result is "+result);
		// subtraction
		result=10-20;
		System.out.println("The result is "+result);
		// multiplication
		result=10*20;
		System.out.println("The result is "+result);
		// Division
		result=10/2;
		System.out.println("The result is "+result);
		// Modulus
		result=10%20;
		System.out.println("The result is "+result);
	}
}
